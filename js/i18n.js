  function ensureLanguagePack(lang) {
    // Strict mode: static UI translations are sourced only from this file.
    // No runtime API fetch and no UI cache hydration.
    if (!lang || !TRANSLATIONS[lang]) return Promise.resolve();
    return Promise.resolve();
  }

  function t(key, lang) {
    var resolvedLang = lang || getLang();
    var dict = TRANSLATIONS[resolvedLang] || {};
    var value = dict[key];

    if (typeof value !== 'undefined') return value;
    if (resolvedLang === 'en' && typeof TRANSLATIONS.en[key] !== 'undefined') return TRANSLATIONS.en[key];
    return key;
  }

