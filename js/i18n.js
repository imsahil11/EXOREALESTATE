  function applyLanguage(lang) {
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (key) {
        el.textContent = t(key, lang);
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (key) {
        el.setAttribute('placeholder', t(key, lang));
      }
    });

    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-title');
      if (key) {
        el.setAttribute('title', t(key, lang));
      }
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria-label');
      if (key) {
        el.setAttribute('aria-label', t(key, lang));
      }
    });

    document.querySelectorAll('[data-i18n-value]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-value');
      if (key) {
        el.setAttribute('value', t(key, lang));
      }
    });

    window.dispatchEvent(new CustomEvent('exo:languageChanged', { detail: { lang: lang } }));
  }

  function setLang(lang) {
    if (!TRANSLATIONS[lang]) lang = 'en';
    localStorage.setItem('exo-lang', lang);
    ensureLanguagePack(lang)
      .catch(function () { return null; })
      .finally(function () { applyLanguage(lang); });
  }

  function readNestedTranslation(obj, lang, field) {
    if (!obj || !obj.translations || !obj.translations[lang]) return '';
    var val = obj.translations[lang][field];
    return typeof val === 'string' && val.trim() ? val : '';
  }

  function isMyMemoryErrorText(value) {
    var s = String(value || '').toUpperCase();
    return s.includes('PLEASE SELECT') || s.includes('QUERY LENGTH') || s.includes('MAX ALLOWED') || s.includes('MYMEMORY');
  }

  var INVALID_STRINGS = [
    'QUERY LENGTH LIMIT EXCEEDED',
    'PLEASE SELECT TWO DISTINCT LANGUAGES',
    'MAX ALLOWED QUERY',
    'MYMEMORY',
    'QUERY LENGTH'
  ];

  function isValidTranslation(text) {
    var value = String(text == null ? '' : text).trim();
    if (!value) return false;
    var upper = value.toUpperCase();
    return !INVALID_STRINGS.some(function (s) {
      return upper.includes(String(s || '').toUpperCase());
    });
  }

  function purgeLocalTranslationCache(cacheKey, value) {
    if (!cacheKey || !value) return;
    if (!isValidTranslation(value)) {
      try { localStorage.removeItem(cacheKey); } catch (e) { }
    }
  }

  async function purgeFirestoreTranslationField(docId, lang, field) {
    if (!window.db || !window.firebase || !docId || !lang || !field) return;
    try {
      var fieldPath = 'translations.' + lang + '.' + field;
      var deletePayload = {};
      deletePayload[fieldPath] = window.firebase.firestore.FieldValue.delete();
      await window.db.collection('properties').doc(docId).update(deletePayload);
    } catch (e) {
      // ignore purge failures (e.g., permission denied for non-owner viewer)
    }
  }

  async function translateChunk(text, targetLang) {
    var input = String(text == null ? '' : text);
    if (!input || !input.trim()) return input;
    if (!targetLang) return input;

    var langPairs = [
      'autodetect|' + targetLang,
      'en|' + targetLang,
      'pt|' + targetLang,
      'fr|' + targetLang,
      'nl|' + targetLang
    ];

    for (var i = 0; i < langPairs.length; i++) {
      var langpair = langPairs[i];
      var sourceLang = String(langpair.split('|')[0] || '').toLowerCase();
      if (sourceLang === String(targetLang).toLowerCase()) continue;

      try {
        var res = await fetch('https://api.mymemory.translated.net/get?q=' + encodeURIComponent(input) + '&langpair=' + encodeURIComponent(langpair));
        var data = await res.json();
        var translated = data && data.responseData ? data.responseData.translatedText : '';
        if (
          data && data.responseStatus === 200 &&
          translated && String(translated).trim() &&
          String(translated) !== input &&
          !isMyMemoryErrorText(translated)
        ) {
          return String(translated);
        }
      } catch (e) {
        continue;
      }
    }

    return input;
  }

  async function translateLongText(text, targetLang) {
    var input = String(text == null ? '' : text);
    if (!input || !input.trim()) return input;
    if (input.length <= 450) return await translateChunk(input, targetLang);

    var chunks = [];
    var i = 0;
    while (i < input.length) {
      var end = Math.min(i + 450, input.length);
      if (end < input.length) {
        var lastPeriod = input.lastIndexOf('.', end);
        var lastNewline = input.lastIndexOf('\n', end);
        var splitAt = Math.max(lastPeriod, lastNewline);
        if (splitAt > i) end = splitAt + 1;
      }
      chunks.push(input.slice(i, end).trim());
      i = end;
    }

    var results = [];
    for (var j = 0; j < chunks.length; j++) {
      var chunk = chunks[j];
      if (!chunk) continue;
      var translated = await translateChunk(chunk, targetLang);
      results.push(translated);
    }
    return results.join(' ').trim() || input;
  }

  async function translatePropertyText(propId, field, text, targetLang, firestoreDocId, propertyDoc) {
    try {
      var sourceText = String(text || '');
      if (!sourceText) return sourceText;
      if (!targetLang) return sourceText;

      var docId = String(firestoreDocId || propId || '').trim();
      var cacheKey = 'trans_' + String(propId || 'global') + '_' + String(field || 'text') + '_' + targetLang;
      var cached = localStorage.getItem(cacheKey);
      if (cached) {
        if (isValidTranslation(cached)) {
          return cached;
        }
        purgeLocalTranslationCache(cacheKey, cached);
      }

      var existingFromDoc = readNestedTranslation(propertyDoc, targetLang, field);
      if (existingFromDoc) {
        if (isValidTranslation(existingFromDoc)) {
          localStorage.setItem(cacheKey, existingFromDoc);
          return existingFromDoc;
        }
        await purgeFirestoreTranslationField(docId, targetLang, field);
        purgeLocalTranslationCache(cacheKey, existingFromDoc);
      }

      var cacheArray = Array.isArray(window.__exoPropertiesCache) ? window.__exoPropertiesCache : [];
      if (!existingFromDoc && cacheArray.length) {
        var fromCache = cacheArray.find(function (p) {
          var pid = String((p && p.id) || '').trim();
          var pdid = String((p && p.__docId) || '').trim();
          return (docId && (pid === docId || pdid === docId)) || (!docId && propId && pid === String(propId));
        });
        var existingFromCache = readNestedTranslation(fromCache, targetLang, field);
        if (existingFromCache) {
          if (isValidTranslation(existingFromCache)) {
            localStorage.setItem(cacheKey, existingFromCache);
            return existingFromCache;
          }
          var cachedDocId = String((fromCache && fromCache.__docId) || (fromCache && fromCache.id) || docId || '').trim();
          await purgeFirestoreTranslationField(cachedDocId, targetLang, field);
          purgeLocalTranslationCache(cacheKey, existingFromCache);
          if (fromCache && fromCache.translations && fromCache.translations[targetLang]) {
            delete fromCache.translations[targetLang][field];
          }
        }
      }

      var fetchedDoc = null;
      if (window.db && docId) {
        try {
          var snap = await window.db.collection('properties').doc(docId).get();
          if (snap && snap.exists) {
            fetchedDoc = snap.data() || {};
            var existingFromFirestore = readNestedTranslation(fetchedDoc, targetLang, field);
            if (existingFromFirestore) {
              if (isValidTranslation(existingFromFirestore)) {
                localStorage.setItem(cacheKey, existingFromFirestore);
                return existingFromFirestore;
              }
              await purgeFirestoreTranslationField(docId, targetLang, field);
              purgeLocalTranslationCache(cacheKey, existingFromFirestore);
            }
          }
        } catch (readErr) {
          // Ignore read failures and continue with API fallback.
        }
      }

      var fieldName = String(field || '').toLowerCase();
      var translated = (fieldName === 'description' || fieldName === 'neighbourhood')
        ? await translateLongText(sourceText, targetLang)
        : await translateChunk(sourceText, targetLang);
      if (!translated || !String(translated).trim()) translated = sourceText;
      if (!isValidTranslation(translated)) translated = sourceText;
      localStorage.setItem(cacheKey, translated);

      if (window.db && docId) {
        try {
          var fieldPath = 'translations.' + targetLang + '.' + field;
          var updatePayload = {};
          updatePayload[fieldPath] = translated;
          await window.db.collection('properties').doc(docId).update(updatePayload);

          // Keep in-memory cache synced for future reads.
          if (Array.isArray(window.__exoPropertiesCache)) {
            var idx = window.__exoPropertiesCache.findIndex(function (p) {
              return String((p && p.id) || '') === docId || String((p && p.__docId) || '') === docId;
            });
            if (idx >= 0) {
              var current = window.__exoPropertiesCache[idx] || {};
              current.translations = current.translations || {};
              current.translations[targetLang] = current.translations[targetLang] || {};
              current.translations[targetLang][field] = translated;
              window.__exoPropertiesCache[idx] = current;
            }
          }
        } catch (writeErr) {
          // Ignore write failures (e.g., permission denied for public viewers).
        }
      }

      return translated;
    } catch (e) {
      return String(text || '');
    }
  }

  function translateListingStatus(status, lang) {
    var s = String(status || '').toLowerCase();
    if (s === 'for sale') return t('cardForSale', lang);
    if (s === 'for rent') return t('cardForRent', lang);
    if (s === 'sold') return t('cardSold', lang);
    if (s === 'new development') return t('cardNewDev', lang);
    if (typeof status === 'string' && status.trim()) return status;
    return t('cardForSale', lang);
  }

